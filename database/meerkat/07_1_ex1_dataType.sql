-- 숫자 데이터 형식
-- INT : 4byte 정수, 범위 +21억 ~ -21억
-- BIGINT : 8byte 정수, 범위 +900경 ~ - 900경
-- FLOAT : 4byte 실수, 소수점 아래 7자리까지 표현
-- DOUBLE : 8byte 실수, 소수점 아래 15자리까지 표현
-- DECIMAL : 5~15byte, 소수점 아래 자리를 지정가능
--  예) DECIMAL(6, 2)

-- 문자 데이터 형식
-- CHAR(n) : 1~255byte, n만큼 고정길이를 가지는 문자형. 고정길이 : 1글자가 들어가도 n값의 영역을 다 사용함.
-- VARCHAR(n) : 1~65535byte, n만큼 가변길이를 가지는 문자형, 한국어 저장가능. 가변길이 : 1글자를 사용하면 n값은 글자수에 따라 영역을 차지함.(최대 4000자)
-- LONGTEXT : 최대 4Gb, text 데이터 값을 저장. 4000자 이상 사용할 때.
-- LONGBLOB : 최대 4Gb, BLOB 데이터 값을 저장. 요즘은 많이 사용하지 않음.
-- ENUM(값1, 값2, 값3....) : 정해진 값만 입력 가능하도록 하는 데이터 형식. maria DB에만 있는 형식.

-- 날짜 및 시간 데이터 형식
-- DATE : 3byte, 'YYYY-MM-DD' 1001-01-01 ~ 9999-12-31 날짜까지만 저장
-- DATETIME : 8byte, 'YYYY-MM-DD hh:mi:ss', 1001-01-01 00:00:00 ~ 9999-12-31 23:59:59 시간까지 저장
-- TIMESTAMP : 4byte,  'YYYY-MM-DD hh:mi:ss', 1970-01-01 00:00:01 ~ 2038-01-19 23:59:59 시간까지 저장
-- **주의 **
-- DATETIME : 서버 시간에 상관없이 고정되는 데이트 타입. 개발자가 작성한 날짜가 고정됨. 어디서 조회해도 작성한 날짜가 됨.
-- TIMESTAMP : 서버 시간에 따라 유동적으로 변하는 데이트 타입. 미국시간, 한국시간 다르게 표시됨.